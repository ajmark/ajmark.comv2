                <h3>Result</h3>
                <p>We presented our findings from the user testing and performing a walkthrough of our prototype to our clients
                and they were happy with the results. We got excellent feedback from them and they were excited to use these ideas 
                in the actual development of their website. Through this project, I learned how user research can have a significant
                impact on a project and spending the extra time to conduct such studies can truly make a difference in the final outcome.</p>

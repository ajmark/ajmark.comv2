                <h3>Process</h3>
                <p>Our goal was to help WiM by designing them a website and developing a functional
                prototype to help them accomplish their organizational objective. The first steps of our design
                process was to determine how to structure their website. We conducted a series of user tests on 
                members of our target audience, women professionals in health care, which included using card sorting
                and label testing techniques.</p>
                <a class="button small" href="../public/assets/projectImages/womenInMedicine/final_paper.pdf">View User Testing Report</a>

                <p>After we finished our user studies, our team was able to begin the design phase of our project.
                This is where we began to develop several iterations of wireframes, high-fidelity mockups, and eventually
                the actual source code for the website prototype.</p>
                <figure>
                    <img class="screenshot-main" src="../public/assets/projectImages/womenInMedicine/wireframe_home.png">

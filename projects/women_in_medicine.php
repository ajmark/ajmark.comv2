                <h2>Women in Medicine</h2>
                <p>This was a semester long project where my team worked with
                a real client to help them create a web presence to promote their
                organization. Our client was the Women in Medicine group of Allegheny
                County (WiM), who's mission is to promote the welfare of women working in the 
                medical profession. The purpose of this website is to provide resources for 
                members of this organization and potential new members in regards to balancing
                family life with work life, professional networking and promoting community involvement.</p>
                <figure>
                    <img class="screenshot-main" src="../public/assets/projectImages/womenInMedicine/homescreen.png">
